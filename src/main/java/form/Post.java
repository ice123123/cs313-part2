package form;

import java.util.Date;

/**
 *
 * @author Iceman
 */
public class Post {
    private String username;
    private String text;
    private Date date;
    
    public Post(String username, String text, Date date){
        this.username = username;
        this.text = text;
        this.date = date;
    }

    public Date getDate() {
        return date;
    }

    public void setDate(Date date) {
        this.date = date;
    }
    
    public String getUsername() {
        return username;
    }

    public void setUsername(String username) {
        this.username = username;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    
}
